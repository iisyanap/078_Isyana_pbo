    package Akademik;

    // MataKuliah.java
    public class MataKuliah {
        private String kodeMK;
        private String namaMK;

        public MataKuliah(String kodeMK, String namaMK) {
            this.kodeMK = kodeMK;
            this.namaMK = namaMK;
        }

        public String getKodeMK() {
            return kodeMK;
        }

        public String getNamaMK() {
            return namaMK;
        }
    }


